/*
                    Comparison Operators
This operator is further divided into two parts
-> Relational Operators   
    < less than
    > greater than
    <= less than or equal to
    >= greater than or equal to

-> Equality Operators
    === strict equality (type + value)
    !== strict non-equality operators (type + value)
    == lose equal to operator (value)
    != lose non-equal to operator (value)
 */

// Relational Operators
console.log("Relational Operators");
console.log(5 < 10);
console.log(5 > 10);
console.log(5 <= 10);
console.log(5 >= 10);

// Equality Operators
console.log("Equality Operators");
console.log(10 === 10);
console.log(10 === "10");
console.log(10 === 20);

console.log(10 !== 10);
console.log(10 !== "10");
console.log(10 !== 20);

console.log(10 == 10);
console.log(10 == "10");

console.log(10 != 10);
console.log(10 != "10");
