// Basic Math

console.log("Arithmetic Operators");
// Arithmetic Operators.
console.log(num + 2);
console.log(num - 2);
console.log(num * 2);
console.log(num / 2);
console.log(num % 2);
console.log(num ** 2);

let num2 = 5;
console.log(num2++); // It increases value after execution
console.log(num2);
console.log(++num2); // It increases value before execution
console.log(num2--); // It decreases value after execution
console.log(num2);
console.log(--num2); // It decreases value before execution
