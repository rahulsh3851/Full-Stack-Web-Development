// Storing Number in Variable
let num = 10;
console.log(num);

num = 1000000;
console.log(num);

// negative number
num = -10;
console.log(num);

// floating number
num = 19.38473;
console.log(num);

// To Check the data type of a variable use
// "typeof" keyword
//  ex- "typeof num"

console.log("Type Operator(typeof)");
// Type Operators.
num = 2;
console.log(typeof num);

